import turtle

t = turtle.Turtle()
t.shape('turtle')

radius = 100
t.circle(radius)           # 반지름이 100인 원을 그린다.
radius = 200
t.circle(radius) 
radius = 50
t.circle(radius) 

t.left(90)
radius = 100
t.circle(radius)           # 반지름이 100인 원을 그린다.
radius = 200
t.circle(radius) 
radius = 50
t.circle(radius) 

t.done()
