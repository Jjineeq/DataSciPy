for i in [1, 2, 3, 4, 5]:         # in 뒤에 리스트를 넣고 끝에 :을 넣자
    print("i =", i)               # i 값을 출력해보자


for i in "Hello":                 # 끝에 :이 있어야 함
    print("i =", i)               # i 값을 출력해보자


for i in [1, 2, 3, 4, 5]:         # 1에서 5까지의 리스트
    print("9 *", i, "=", 9 * i)   # 9*i 값을 출력해보자
